# type casting = convert the data type of a value to another data type

x = 1 # int
y = 2.0 # float
z = "3" # str

x = str(x) # from integer to string 
y = str(y) # from float to string
z = int(z) # from string to integer

w = str(z) # from integer to string

print(x)
print(y)
print(z * 3)
print(w * 3)